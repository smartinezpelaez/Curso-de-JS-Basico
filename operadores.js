//operadores aritméticos

// Suma
var sum = 2 + 3 // 5
// Resta
var res = 5 - 3 // 2
// Multiplicación
var multi = 4 * 2 // 8
// División
var div = 6 / 2 // 3
//Residuo
var resi = 21 % 5 // 1
console.log(`operaciones ${sum}; ${res}; ${multi}; ${div}; ${resi}`)

//operador de concatenación

var str = "hola" + "mundo";
